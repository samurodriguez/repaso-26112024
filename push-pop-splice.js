const fruits = ["naranja", "pera", "manzana"];

// Push. Introduce los valores que le pasemos por parámetro al final del array

fruits.push("limón");

console.log(fruits);

// Pop. Elimina el último valor del array

fruits.pop();

console.log(fruits);

// Splice. Elimina la cantidad de elementos que queramos desde x posición

fruits.splice(1, 2);

console.log(fruits);
